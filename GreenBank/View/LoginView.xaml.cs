namespace GreenBank.View
{
    /// <summary>
    /// Interaction logic for LoginView.xaml
    /// </summary>
    public partial class LoginView : Window
    {
        public LoginView()
        {
            InitializeComponent();
        }

        private void Window_MouseDown(object sender, MouseButtonEventArgs e)
        {

            if (e.LeftButton == MouseButtonState.Pressed)
                DragMove();
        }

        private void btnClose_Click(object sender, RoutedEventArgs e)
        {
            Application.Current.Shutdown();
        }

        private void btnMinimize_Click(object sender, RoutedEventArgs e)
        {
            WindowState = WindowState.Minimized;
        }

        private void btnLogin_Click(object sender, RoutedEventArgs e)
        {
            if (LoginProperty.LoginCheck(txtUser.Text,txtPass.Password)=="employee")
            {
                MainWindow mainWindow = new MainWindow();
                mainWindow.employee_id = txtUser.Text;
                mainWindow.Show();
                this.Close();
            }
            else if(LoginProperty.LoginCheck(txtUser.Text, txtPass.Password) == "customer")
            {
                MessageBox.Show("Müşteri Girişi");
            }
        }
    }
}
